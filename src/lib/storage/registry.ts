// --- Registry entry type -----------------------------------------------------

export interface RegistryEntry<T = unknown> {
  id: StorageKeyId;
  key: string;
  /** Schema used to validate the deserialized value on load. */
  schema: z.ZodType<T>;
  /** Value returned on absence or corruption (must satisfy schema). */
  default: T;
  /** 'json' (default) round-trips through JSON; 'raw' stores the string verbatim. */
  serialize?: 'json' | 'raw';
  /** Secret entries are skipped during backup export. */
  secret?: boolean;
  /** Schema/migration version. Bump when the persisted shape changes. */
  version: number;
  /**
   * Optional migration: receives parsed-but-unknown data plus the version it was
   * stored under, returns data ready for `schema.safeParse`. Used on import of
   * older backups. Identity by default.
   */
  migrate?: (data: unknown, fromVersion: number) => unknown;
}

// Helper to build an entry while keeping `default` typed to the schema output.
function entry<S extends z.ZodType>(
  e: Omit<RegistryEntry<z.infer<S>>, 'schema'> & { schema: S },
): RegistryEntry<z.infer<S>> {
  // `version` is required on every entry below, so only `serialize` needs a
  // default here. (Spreading a `version` default would be overwritten anyway.)
  return { serialize: 'json', ...e } as RegistryEntry<z.infer<S>>;
}

