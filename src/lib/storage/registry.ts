// ==============================================================================
// AI Launcher Pro - Storage Registry (central key catalog)
// Every persisted localStorage key the app knows about, each with:
//   - id        : stable identifier (key of STORAGE_KEYS)
//   - key       : the real localStorage key string
//   - schema    : zod schema used to validate on load (boundary validation)
//   - default   : value returned when the key is absent OR corrupted
//   - serialize : 'json' (default) or 'raw' for plain-string values
//   - secret    : true for entries that must NOT be exported in backups
//   - version   : schema/migration version (bump + add a migrate() when shape changes)
//
// The registry is the SINGLE source of truth for config export/import: backups
// are generated by walking REGISTRY, not a hardcoded list. This guarantees that
// every known key is included in a backup (the v15 bug: configIO only dumped
// providers/presets/config/font and silently dropped profiles, workspaces,
// runbooks, budget, custom CLIs/IDEs, overrides, cli-order and theme).
//
// Schemas are intentionally TOLERANT (.passthrough / .catchall(unknown)) — the
// goal here is boundary validation (corrupt blob -> default, never crash), not
// strict shape enforcement. Stricter validation lives in each domain module.
// ==============================================================================

