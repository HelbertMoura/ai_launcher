export type RegistryId = keyof typeof REGISTRY;

/**
 * Keys that participate in config backups (export/import). Excludes secrets and
 * one-shot/transient flags that should not travel between machines.
 *
 * Intentionally NOT in the registry (and therefore never in a backup):
 *   - per-provider transient state: "ai-launcher:provider-test:<id>"
 *   - caches: "ai-launcher:updates-cache", ":clis-cache", ":tools-cache"
 *   - dynamic dirs: "ai-launcher:pinned-dirs:<cliKey>"
 *   - "*-changed" storage-event signals (customClis/customIdes/clisOverrides)
 *   - secrets: "ai-launcher-secret:*" (owned by lib/secrets.ts + providers/storage.ts)
 *   - legacy "ai-launcher:session-templates" (read-only during migration)
 */
const NON_BACKUP_IDS: ReadonlySet<RegistryId> = new Set<RegistryId>([
  'profilesMigrated', // one-shot migration flag — machine-local
  'onboardingDone', // first-run UI flag — machine-local
  'showOnboarding', // first-run UI flag — machine-local
]);

export function backupEntries(): RegistryEntry[] {
  return Object.values(REGISTRY).filter(
    (e) => !e.secret && !NON_BACKUP_IDS.has(e.id as RegistryId),
  ) as RegistryEntry[];
}

export function entryById(id: RegistryId): RegistryEntry {
  return REGISTRY[id] as RegistryEntry;
}
